class Student
{
int sid;
String sName;
int marks;
Student()
{
System.out.println(sid+" "+sName+" "+marks);
}
Student(int sid)
{
System.out.println(sid+" "+sName+" "+marks);
}
Student(int sid, String sName)
{
System.out.println(sid+" "+sName+" "+marks);
}
Student(int sid, String sName, int marks)
{
System.out.println(sid+" "+sName+" "+marks);
}
}